
public abstract class Animal
{
    public Animal()
    {
        System.out.println("Animal Constructor");
    }

    public void Greet()
    {
    	System.out.println("Animal says Hello");
    }

    public void Talk()
    {
    	System.out.println("Animal Talking");
    }

    public void Sing()
    {
    	System.out.println("Animal sing");
    }
}
